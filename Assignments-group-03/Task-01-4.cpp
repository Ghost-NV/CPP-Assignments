#include <iostream>
using namespace std;
int main() {

  // Comparison Operators (0 = false , true = 1)
  cout << (100 == 100) << endl; // Equal
  cout << (100 != 100) << endl; // Not equal
  cout << (100 < 90) << endl; // Greater than
  cout << (100 > 90) << endl; // Less than
  cout << (100 >= 100) << endl; // Less than or equal
  cout << (100 <= 100) << endl; // Greater than or equal

  return 0;
}